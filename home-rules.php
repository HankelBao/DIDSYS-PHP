            <h2>纪检部每⽇日常规及自主劳动检查评分细则</h2>
            一、班级卫⽣</br>
            <div style="padding-left:40px">
                1.地面整洁，无垃圾或杂物，地面⽆无污垢 </br>
                2.桌椅及地上的物品摆放整⻬齐有序 </br>
                3.垃圾桶内不不得有⼤大量量堆积的垃圾，不不得有异味（垃圾桶过半提醒，过三 分之⼆二扣分） </br>
                4.⿊黑板版面⼲干净，不不得有过多板书与粉笔灰痕迹，允许有少量量⽂文字有序写 在⿊黑板上。讲台上不不得有⼤大量量粉笔灰与杂物乱放 </br>
                5.柜⼦子以及书架中物品需摆放整⻬齐，其表⾯面⽆无灰尘且⼲干净；扫帚拖把排放整齐，拖把桶⾥里里不不得有污⽔水 </br>
                6.附加项：若班级内⽆无⼈人但是电灯开着，在当天得分的基础上还会再扣掉 一分。 </br>
                注：</br>
                1.班级时间检查时间为周一到周四下午5:10到5:30 </br>
                2.每天卫⽣生总分为10分，以上五项标准各占2分 </br>
                3.每天各班卫⽣生分数将于当⽇日17点30分后公布于一楼⿊黑板处以及纪检部⽹网 站上 </br>
                4.检查时间之外纪检部会进⾏行行不不定期抽查 </br>
                5.检查员检查完后会将扣分的原因与分数写在⿊黑板上，并将扣分单移交其项⽬目负责⼈人处  </br>
            </div>
            二、眼保健操</br>
            <div style="padding-left:40px">
                1.眼保健操时间内不不得随意⾛走动，睡觉，睁眼，打扫卫⽣生等做与眼保健操无关的事 </br>
                2.1⾄至2⼈人不不做扣0.5分，3⾄至4⼈人不不做扣1分，以此类推。     </br>
                注：     </br>
                1. 若有⼴广播损坏等特殊情况需向检察员说明，并及时保修，由班级班委喊口令  </br>
                2. 每天各班眼保健操总分为10分   </br>
                3.每天各班眼保健操分数将于当天17点30分后公布于⼀一楼⿊黑板处以及纪检部网站上    </br>
                4. 检查员检查完后会将扣分的原因与分数写在黑板上，并将扣分单移交 其项⽬目负责⼈人处    </br>
                5.由于国际部⼤大规模的⾛走班⼈人数，眼保健操的检查将仅在⼤大陆部实行 </br>
            </div>
            三、大课间</br>
            <div style="padding-left:40px">
            1.跑操</br>
                <div style="padding-left:40px">
                    a.队列整齐有序，班级中没有抢跑，落队等情况</br>
                    b.与前面的班级间距控制得当，速度保持恰当</br>
                    c.除去特殊情况，不得有大面积缺席，特殊情况与请假需与检察员说明</br>
                    d.要求着装校服，不得穿戴围巾，自己的外套与帽子等</br>
                    e.进退场快速并有序</br>
                </div>
            以上每列为2分</br>
            2.广播体操</br>
                <div style="padding-left:40px">
                a.队列整齐有序</br>
                b.除去特殊情况，不得有大面积缺席，特殊情况与请假需与检察员说明</br>
	            c.做操时，动作标准有力</br>
                d. 要求着装校服，不得穿戴围巾，自己的外套与帽子等</br>
                e.进退场快速并有序</br>
                </div>
            3.升旗仪式</br>
                <div style="padding-left:40px">
    	        a.要求穿着校服	</br>
	            b.升国旗升校旗时行注目礼，不得随意讲话</br>
                </div>
            注：</br>
                <div style="padding-left:40px">
	            1.每天课间操满分为10分</br>
	            2. 每天各班课间操分数将于当日17点30分后公布于一楼黑板处以及纪检部网站上</br>
                </div>
            </div>
            四、餐厅卫生
            <div style="padding-left:40px">
                1.桌面整洁，不得有剩余饭菜，垃圾纸屑，</br>
                2.就餐结束后将椅子塞入桌内，如有大面积椅子未塞则扣分</br>
                注: </br>
                1.餐厅满分为10分，扣完为止</br>
                2.餐厅分数将于当日17点30分后公布于一楼黑板处以及纪检部网站上</br>
                3.由于大部分同学晚饭时间不固定，餐厅卫生只在中午进行检查</br>
                4.为方便纪检员进行检查，请同学们坐在本班区域内用餐！！</br>
            </div>
            五、自主劳动
            <div style="padding-left:40px">
                1.台面清理要求无积水无垃圾，物品摆放整齐   </br>
                2.蹲位清理要求无异味无污垢，周围地面清洗干净</br>
                3.小便池清理要求无异味无污垢             </br>
                4.地面清洗要求干净无大量水渍，无污垢与脚印      </br>
                5.工具摆放要求摆放整齐并保持干净，用完需清洗 </br>
            </div>
